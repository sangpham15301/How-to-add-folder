    private void lb_signupMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lb_signupMousePressed
        int id;
        String user = tf_username.getText();
        String pw   = pf_password.getText();
        Connection connection = SignInFrame.getJDBCConnection();

        try 
        {
            String sql = "select *from form";
            Statement stmt = connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery(sql);
            rs.last();
            id = rs.getInt(1);
            rs.absolute(0);
            while(rs.next())
            {
                if(rs.getString(2).equals(user))
                {
                    if(rs.getString(3).equals(pw))
                    {
                        this.dispose();
                        new Message_Frame().setVisible(true);
                        break;
                    }
                }
                if(rs.getInt(1) == id)
                {
                    lb_notification.setText("Do you forget password?");
                    lb_notification.setForeground(Color.red);
                    lb_notification.setVisible(true);
                }
            }
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(SignInFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_lb_signupMousePressed

