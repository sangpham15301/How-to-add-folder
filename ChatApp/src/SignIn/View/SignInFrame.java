    private void lb_signinMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lb_signinMousePressed
        Connection connection = SignInFrame.getJDBCConnection();
        try {
            Statement stmt = connection.createStatement();
            String sql = "insert into form(username,password) values(?,?)";
            PreparedStatement ps = connection.prepareStatement(sql);
//            ps.setInt(1,1);
            ps.setString(1,tf_username.getText());
            ps.setString(2,pf_password.getText());
            
            ps.executeUpdate();
            stmt.close();
            connection.close();
        } catch (SQLException ex) {
            Logger.getLogger(SignInFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        tf_username.setText("User Name");
        pf_password.setText("Password");
        
        lb_notification.setText("You signed in successfully!");
        lb_notification.setForeground(Color.green);
        lb_notification.setVisible(true);
    }//GEN-LAST:event_lb_signinMousePressed

